package com.java.rush.Level_2.Lesson_2;

public class AandB {
    public static void main(String[] args) {
        int a = 5;
        int b = 7;
        System.out.println(summ(a, b));
        System.out.println(mul(a, b));
    }
    public static int summ(int a, int b){
        return a + b;

    }
    public static int mul(int a, int b){
        return a * b;
    }


}
